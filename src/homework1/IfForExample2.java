package homework1;

public class IfForExample2 {
    public static void main(String[] args) {
        int a = 5, b = 15; // b-ի արժեքը նշված է, որպիսի հնարավոր
        // լինի պարզել խնդիրը ճիշտ լուծված է, թե ոչ:
        for (a = 5; a <= 15; a++) {
            System.out.println(+a);
        }
    }
}